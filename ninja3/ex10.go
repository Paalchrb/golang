package main

import "fmt"

func main() {
	fmt.Printf("true && true equals %v\n", true && true)
	fmt.Printf("true && false equals %v\n", true && false)
	fmt.Printf("true || true equals %v\n", true || true)
	fmt.Printf("true || true equals %v\n", true || true)
	fmt.Printf("!true equals %v\n", !true)
}